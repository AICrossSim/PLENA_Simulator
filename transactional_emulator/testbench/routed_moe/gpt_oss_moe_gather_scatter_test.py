    if args.token_index < 0 or args.token_index >= rows:
        raise ValueError(f"token_index={args.token_index} out of range [0, {rows})")

    slot_rows = blen
    token_offsets = torch.tensor([args.token_index * hidden], dtype=torch.int32)
