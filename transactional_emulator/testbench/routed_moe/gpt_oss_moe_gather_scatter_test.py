def _clamp_counts_from_slots(
    *,
    token_indices: list[int],
    gate: torch.Tensor,
    up: torch.Tensor,
    blen: int,
    rows: int,
) -> dict:
    per_token = [
        {
            "token_index": token_idx,
            "gate_gt_7": 0,
            "gate_lt_neg_7": 0,
            "up_gt_7": 0,
            "up_lt_neg_7": 0,
        }
        for token_idx in range(rows)
    ]
    for pair_idx, token_idx in enumerate(token_indices):
        active = pair_idx * blen
        per_token[token_idx]["gate_gt_7"] += int((gate[active] > 7).sum().item())
        per_token[token_idx]["gate_lt_neg_7"] += int((gate[active] < -7).sum().item())
        per_token[token_idx]["up_gt_7"] += int((up[active] > 7).sum().item())
        per_token[token_idx]["up_lt_neg_7"] += int((up[active] < -7).sum().item())
    return {
        "gate_gt_7": sum(item["gate_gt_7"] for item in per_token),
        "gate_lt_neg_7": sum(item["gate_lt_neg_7"] for item in per_token),
        "up_gt_7": sum(item["up_gt_7"] for item in per_token),
        "up_lt_neg_7": sum(item["up_lt_neg_7"] for item in per_token),
        "per_token": per_token,
    }


