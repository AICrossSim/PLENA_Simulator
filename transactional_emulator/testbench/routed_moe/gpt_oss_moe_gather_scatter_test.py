def run_full_vram_stage(args: argparse.Namespace) -> dict:
    """Run Step5 segment A, optionally followed by HBM storeback validation."""
    mlen = args.mlen
    blen = args.blen
    build_dir = args.build_dir.expanduser().resolve()
