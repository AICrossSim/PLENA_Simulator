    if topk_indices.shape != topk_weights.shape:
        raise ValueError(f"topk indices/weights shape mismatch: {topk_indices.shape} vs {topk_weights.shape}")

    tensors = load_layer_tensors(args.layer_index)
    split = split_packed_gate_up(tensors["gate_up_weight"], tensors["gate_up_bias"])
    down_weight = tensors["down_weight"]
    down_bias = tensors["down_bias"]
    intermediate = int(split.gate_weight.shape[-1])

    selected_experts = [int(v) for v in torch.unique(topk_indices).sort().values.tolist()]
    if args.only_experts:
        requested = [int(part) for part in args.only_experts.split(",") if part.strip()]
        missing = sorted(set(requested) - set(selected_experts))
        if missing:
            raise ValueError(f"--only-experts requested unrouted experts: {missing}")
        selected_experts = requested
    expert_specs = []
    max_slot_rows = blen
    int_preload_values: list[int] = []

    prog = PlenaCompiler(mlen=mlen, blen=blen, real_data_ratio=hw.real_data_ratio)
    zero = prog.fp_var("zero", size=1)
    limit_pos = prog.fp_var("gpt_oss_limit_pos", size=max(blen, rows * blen))
    limit_neg = prog.fp_var("gpt_oss_limit_neg", size=max(blen, rows * blen))
    one = prog.fp_var("one", size=max(blen, rows * blen))
    neg_alpha = prog.fp_var("neg_alpha", size=max(blen, rows * blen))
